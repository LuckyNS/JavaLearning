class Student {
	
  private String name;
  private int age;
  
  
  //Creating getter (get value) 
  public String getName(){
    return name;
  }
  
  //Creating setter (set value)
  public void setName(String name){
    this.name = name;
  }
  
  public int getAge(){
    return age;
  }
  
  public void setAge(int age){
    this.age = age;
  }
  
}